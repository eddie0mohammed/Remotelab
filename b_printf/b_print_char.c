#include <unistd.h>

void	ft_wri(char c)
{
	write(1, &c, 1);
}

int		b_print_char(char ch)
{
	int count;

	count = 0;
	if (ch)
	{
		ft_wri(ch);
		count++;
	}
	return (count);
}
